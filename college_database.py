def get_institution_by_id(institution_id):
    """Get institution details by ID"""
    for institution in TAMILNADU_COLLEGES_DATABASE:
        if institution['institution_id'] == institution_id:
            return institution
    return None

def search_institutions(query=None, category=None, type_filter=None):
    """Search institutions by various criteria"""
    results = TAMILNADU_COLLEGES_DATABASE.copy()
    
    if query:
        query_lower = query.lower()
        results = [inst for inst in results 
                  if query_lower in inst['name'].lower() 
                  or query_lower in inst.get('affiliation', '').lower()
                  or any(query_lower in course.lower() for course in inst['courses'])]
    
    if category:
        results = [inst for inst in results if inst.get('category') == category]
    
    if type_filter:
        results = [inst for inst in results if inst.get('type') == type_filter]
    
    return results

def get_institution_categories():
    """Get unique institution categories"""
    categories = set()
    for institution in TAMILNADU_COLLEGES_DATABASE:
        if 'category' in institution:
            categories.add(institution['category'])
    return sorted(list(categories))

def get_institution_types():
    """Get unique institution types"""
    types = set()
    for institution in TAMILNADU_COLLEGES_DATABASE:
        if 'type' in institution:
            types.add(institution['type'])
    return sorted(list(types))

def get_courses_by_institution(institution_id):
    """Get courses offered by a specific institution"""
    institution = get_institution_by_id(institution_id)
    return institution['courses'] if institution else []

